#include "libft.h"

int	strncmp(const char *s1, const char *s2, size_t n)
{
	while (n > 0)
	{
		if (*s1 != *s2)
		{
			return ((*s1 - *s2));
		}
		else
		{
			s1++;
			s2++;
		}
		n--;
	}
	return (0);
}
